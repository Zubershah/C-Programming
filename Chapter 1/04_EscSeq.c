#include <stdio.h>
int main()
{
    printf("hello world\n");        /* \n ---> new line */
    printf("hello \t world\n");     /* \t ---> Tab(Space) */
    printf("hello \\ world\n");     /* \\ ---> \ */
    printf("hello \" world\n");     /* \" ---> " */
    printf("hello \' world\n");     /* \' ---> ' */
    return 0;
}