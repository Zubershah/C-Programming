#include <stdio.h>
int main()
{
    int a;
    a = 7;

    float c;
    c = 7.5;

    char e;
    e = 's';

    printf("the value a is %d\n", a); // the value a is 7
    printf("the value c is %f\n", c); // the value c is 7.5
    printf("the character is %c", e); // the character is s
    return 0;
}